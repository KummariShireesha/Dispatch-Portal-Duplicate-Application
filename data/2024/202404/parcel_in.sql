CREATE TABLE PARCEL_IN(
    IN_ENTRY_ID NUMBER PRIMARY KEY,
    ENTRY_DATE_TIME TIMESTAMP(6) NOT NULL,
    SENDER_NAME VARCHAR2(20) NOT NULL,
    SENDER_DEPARTMENT VARCHAR2(20) NOT NULL,
    SENDER_LOCATION VARCHAR2(20) NOT NULL,
    SENDER_MOBILE_NUMBER NUMBER(10),
    TO_WHOM VARCHAR2(20) NOT NULL,
    RECIPIENT_DEPARTMENT VARCHAR2(20) NOT NULL,
    RECIPIENT_PHONE_NUMBER NUMBER(10),
    MODE_OF_RECEIPT VARCHAR2(20) NOT NULL,
    BARCODE_NO VARCHAR2(20) UNIQUE
);
====================================================================

CREATE SEQUENCE PARCEL_IN_S
  START WITH 1
 INCREMENT BY 1 minvalue 1 maxvalue 9999999999999;
 